function initLanguageSwitcher() {
    const container = document.getElementById('google_translate_element');
    if (!container) return;

    // Clear Google Translate if it exists
    container.innerHTML = '';

    const currentLang = localStorage.getItem('site_lang') || 'en';
    const currentLangObj = languages.find(l => l.code === currentLang) || languages[0];

    // Create Custom Dropdown
    const btn = document.createElement('button');
    btn.className = 'custom-lang-btn';
    btn.innerHTML = `${currentLangObj.flag} ${currentLangObj.name} <i class="fas fa-chevron-down" style="font-size: 0.8rem; margin-left: 5px;"></i>`;
    
    const dropdown = document.createElement('div');
    dropdown.className = 'custom-lang-dropdown';
    
    languages.forEach(lang => {
        const option = document.createElement('div');
        option.className = 'lang-option';
        option.innerHTML = `<span style="font-size: 1.2rem;">${lang.flag}</span> ${lang.name}`;
        option.onclick = () => setLanguage(lang.code);
        dropdown.appendChild(option);
    });

    // Toggle Dropdown
    btn.onclick = (e) => {
        e.stopPropagation();
        dropdown.classList.toggle('show');
    };

    // Close on click outside
    document.addEventListener('click', () => {
        dropdown.classList.remove('show');
    });

    container.appendChild(btn);
    container.appendChild(dropdown);
}

function setLanguage(langCode) {
    localStorage.setItem('site_lang', langCode);
    location.reload(); // Reload to apply changes cleanly
}

function applyTranslation() {
    const lang = localStorage.getItem('site_lang') || 'en';
    if (lang === 'en') return; // Default is English

    const dict = translations[lang];
    if (!dict) return;

    // Helper to walk DOM and replace text
    function walk(node) {
        if (node.nodeType === 3) { // Text node
            const text = node.nodeValue.trim();
            if (text && dict[text]) {
                node.nodeValue = dict[text];
            } else if (text) {
                // Try partial replacement for sentences if exact match fails
                // Note: This is risky, keeping it simple for now to exact matches or exact substrings
                for (const [key, value] of Object.entries(dict)) {
                    if (text === key) {
                        node.nodeValue = value;
                        break;
                    }
                }
            }
        } else if (node.nodeType === 1 && node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE') {
            // Check placeholders for inputs
            if (node.tagName === 'INPUT' || node.tagName === 'TEXTAREA') {
                const ph = node.getAttribute('placeholder');
                if (ph && dict[ph]) {
                    node.setAttribute('placeholder', dict[ph]);
                }
            }
            
            // Recursively walk children
            for (let i = 0; i < node.childNodes.length; i++) {
                walk(node.childNodes[i]);
            }
        }
    }

    walk(document.body);
    
    // Fix RTL for Urdu
    if (lang === 'ur') {
        document.body.style.direction = 'rtl';
        document.body.style.textAlign = 'right';
    } else {
        document.body.style.direction = 'ltr';
        document.body.style.textAlign = 'left';
    }
}

(function () {
    const defaults = { site_name: 'RewardLoop', currency_symbol: 'PKR', referral_signup_bonus: 0 };
    let settings = { ...defaults };

    function getCurrencySymbol() {
        return (settings.currency_symbol || defaults.currency_symbol || 'PKR').toString().trim() || 'PKR';
    }

    function getSiteName() {
        return (settings.site_name || defaults.site_name || 'RewardLoop').toString().trim() || 'RewardLoop';
    }

    function replaceTextValue(value) {
        if (typeof value !== 'string' || value.length === 0) return value;
        let out = value;
        const cur = getCurrencySymbol();
        const name = getSiteName();
        if (cur && cur !== 'PKR') out = out.replace(/\bPKR\b/g, cur);
        if (name && name !== 'RewardLoop') out = out.replace(/\bRewardLoop\b/g, name);
        return out;
    }

    function applyBrandingToNode(node) {
        if (!node) return;

        if (node.nodeType === Node.TEXT_NODE) {
            const next = replaceTextValue(node.nodeValue);
            if (next !== node.nodeValue) node.nodeValue = next;
            return;
        }

        if (node.nodeType !== Node.ELEMENT_NODE) return;
        const el = node;
        const tag = el.tagName;
        if (tag === 'SCRIPT' || tag === 'STYLE') return;

        if (tag === 'INPUT' || tag === 'TEXTAREA') {
            const ph = el.getAttribute('placeholder');
            if (ph) {
                const nextPh = replaceTextValue(ph);
                if (nextPh !== ph) el.setAttribute('placeholder', nextPh);
            }
            if (typeof el.value === 'string') {
                const nextVal = replaceTextValue(el.value);
                if (nextVal !== el.value) el.value = nextVal;
            }
        }

        if (tag === 'META') {
            const content = el.getAttribute('content');
            if (content) {
                const nextContent = replaceTextValue(content);
                if (nextContent !== content) el.setAttribute('content', nextContent);
            }
        }

        const walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT, {
            acceptNode: (t) => {
                if (!t.nodeValue) return NodeFilter.FILTER_REJECT;
                if (t.nodeValue.includes('PKR') || t.nodeValue.includes('RewardLoop')) return NodeFilter.FILTER_ACCEPT;
                return NodeFilter.FILTER_REJECT;
            }
        });

        const textNodes = [];
        while (walker.nextNode()) textNodes.push(walker.currentNode);
        for (const t of textNodes) applyBrandingToNode(t);
    }

    function applyBranding() {
        applyBrandingToNode(document.head);
        applyBrandingToNode(document.body);
    }

    async function fetchSettings() {
        try {
            const res = await fetch('/api/admin/settings/general', { credentials: 'same-origin' });
            if (res.ok) return await res.json();
        } catch {}

        try {
            const res = await fetch('/api/settings/general', { credentials: 'same-origin' });
            if (res.ok) return await res.json();
        } catch {}

        return {};
    }

    function startObserver() {
        const obs = new MutationObserver((mutations) => {
            obs.disconnect();
            try {
                for (const m of mutations) {
                    if (m.type === 'characterData') {
                        applyBrandingToNode(m.target);
                    } else if (m.addedNodes && m.addedNodes.length) {
                        m.addedNodes.forEach(applyBrandingToNode);
                    }
                }
            } finally {
                obs.observe(document.documentElement, { subtree: true, childList: true, characterData: true });
            }
        });
        obs.observe(document.documentElement, { subtree: true, childList: true, characterData: true });
    }

    async function init() {
        const loaded = await fetchSettings();
        settings = { ...defaults, ...loaded };
        window.APP_SETTINGS = settings;
        window.getCurrencySymbol = getCurrencySymbol;
        window.getSiteName = getSiteName;
        window.formatCurrency = (amount, mode = 'suffix') => {
            const cur = getCurrencySymbol();
            const v = typeof amount === 'number' ? amount : Number(amount);
            const val = Number.isFinite(v) ? v : amount;
            return mode === 'prefix' ? `${cur} ${val}` : `${val} ${cur}`;
        };
        applyBranding();
        startObserver();
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
